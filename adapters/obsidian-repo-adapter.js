const fs = require('fs');
const path = require('path');
const { exec } = require('child_process');
const { promisify } = require('util');

const execAsync = promisify(exec);

class ObsidianRepoAdapter {
  constructor(baseDir) {
    this.baseDir = baseDir || process.cwd();
  }

  async readIssue(issueId) {
    const issuesDir = path.join(this.baseDir, '.work', 'issues');
    try {
      const dirs = fs.readdirSync(issuesDir);
      const match = dirs.find(d => d.startsWith(`${issueId}-`));
      if (!match) return { id: issueId, title: '', body: '', labels: [] };

      const contextPath = path.join(issuesDir, match, 'shared', 'context.md');
      const context = fs.readFileSync(contextPath, 'utf-8');
      return { id: issueId, title: context.split('\n')[0]?.replace('# Context: ', '') || '', body: context, labels: [] };
    } catch {
      return { id: issueId, title: `Issue #${issueId}`, body: '', labels: [] };
    }
  }

  async fetchContext(repoName) {
    return { repo: repoName, dir: this.baseDir, type: 'obsidian' };
  }

  async writeFile(filePath, content) {
    const fullPath = path.join(this.baseDir, filePath);
    fs.mkdirSync(path.dirname(fullPath), { recursive: true });
    fs.writeFileSync(fullPath, content, 'utf-8');
  }

  async createPR(options) {
    try {
      await execAsync(`git checkout -b ${options.branch}`, { cwd: this.baseDir });
      await execAsync('git add -A', { cwd: this.baseDir });
      await execAsync(`git commit -m "${options.title}" --allow-empty`, { cwd: this.baseDir });
      await execAsync(`git push origin ${options.branch}`, { cwd: this.baseDir });

      const { stdout } = await execAsync(
        `gh pr create --title "${options.title}" --body "Auto-generated by Unified AI OS" --base main`,
        { cwd: this.baseDir }
      );
      return { url: stdout.trim() };
    } catch (err) {
      return { url: null, error: err.message };
    }
  }
}

module.exports = { ObsidianRepoAdapter };
